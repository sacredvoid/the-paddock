// ---------------------------------------------------------------------------
// Track coordinate paths for F1 circuits (normalized 0-1 range)
// Each path is an array of {x, y} points that approximate the real layout.
// ---------------------------------------------------------------------------

export interface TrackPoint {
  x: number;
  y: number;
}

// Default oval fallback for unknown circuits
const DEFAULT_OVAL: TrackPoint[] = (() => {
  const pts: TrackPoint[] = [];
  const steps = 40;
  for (let i = 0; i < steps; i++) {
    const angle = (2 * Math.PI * i) / steps;
    pts.push({
      x: 0.5 + 0.4 * Math.cos(angle),
      y: 0.5 + 0.25 * Math.sin(angle),
    });
  }
  return pts;
})();

